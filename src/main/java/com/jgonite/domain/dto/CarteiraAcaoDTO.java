package com.jgonite.domain.dto;

public class CarteiraAcaoDTO {

	private String acao;
	private String qtdAcoes;
	private String valorInvestido;
	private String valorAtual;
	private String valorProventos;
	private String precoMedio;
	private String ROI;
	private String percCarteira;
	
	public String getAcao() {
		return acao;
	}
	public void setAcao(String acao) {
		this.acao = acao;
	}
	public String getQtdAcoes() {
		return qtdAcoes;
	}
	public void setQtdAcoes(String qtdAcoes) {
		this.qtdAcoes = qtdAcoes;
	}
	public String getValorInvestido() {
		return valorInvestido;
	}
	public void setValorInvestido(String valorInvestido) {
		this.valorInvestido = valorInvestido;
	}
	public String getValorAtual() {
		return valorAtual;
	}
	public void setValorAtual(String valorAtual) {
		this.valorAtual = valorAtual;
	}
	public String getValorProventos() {
		return valorProventos;
	}
	public void setValorProventos(String valorProventos) {
		this.valorProventos = valorProventos;
	}
	public String getPrecoMedio() {
		return precoMedio;
	}
	public void setPrecoMedio(String precoMedio) {
		this.precoMedio = precoMedio;
	}
	public String getROI() {
		return ROI;
	}
	public void setROI(String rOI) {
		ROI = rOI;
	}
	public String getPercCarteira() {
		return percCarteira;
	}
	public void setPercCarteira(String percCarteira) {
		this.percCarteira = percCarteira;
	}
	
	
	
}
